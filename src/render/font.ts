export const GLYPH_WIDTH = 5 as const
export const GLYPH_HEIGHT = 7 as const
export const GLYPH_GAP = 1 as const

/** Every character used by HUD, attract, banners, and name entry. */
export const REQUIRED_CHARSET = "ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789 !.:-" as const

export const measureText = (text: string, scale: number): number =>
  text.length === 0 ? 0 : (text.length * (GLYPH_WIDTH + GLYPH_GAP) - GLYPH_GAP) * scale

export const drawText = (
  ctx: CanvasRenderingContext2D,
  text: string,
  x: number,
  y: number,
  color: string,
  scale = 1,
): void => {
  ctx.fillStyle = color
  let cursor = x
  for (const ch of text) {
    const glyph = GLYPHS[ch]
    if (glyph) {
      for (let row = 0; row < GLYPH_HEIGHT; row++) {
        const line = glyph[row]
        if (!line) continue
        for (let col = 0; col < GLYPH_WIDTH; col++) {
          if (line[col] === "#") {
            ctx.fillRect(cursor + col * scale, y + row * scale, scale, scale)
          }
        }
      }
    }
    cursor += (GLYPH_WIDTH + GLYPH_GAP) * scale
  }
}

export const drawTextCentered = (
  ctx: CanvasRenderingContext2D,
  text: string,
  centerX: number,
  y: number,
  color: string,
  scale = 1,
): void => {
  drawText(ctx, text, Math.round(centerX - measureText(text, scale) / 2), y, color, scale)
}
